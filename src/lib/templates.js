export function formatTemplate(templateKey, answers) {
    const today = new Date();
    const dd = String(today.getDate()).padStart(2, "0");
    const mm = String(today.getMonth() + 1).padStart(2, "0");
    const yyyy = String(today.getFullYear());
    const vars = { ...answers, today: `${dd}.${mm}.${yyyy}` };
    const tpl = TEMPLATES[templateKey];
    if (!tpl) return "";
    const body = Array.isArray(tpl.body) ? tpl.body.join("") : tpl.body;
    return body.replace(/\{(.*?)\}/g, (_, k) => String(vars[k] ?? ""));
}

export function slugify(text) {
    return String(text)
        .toLowerCase()
        // Разрешаем латиницу, кириллицу (в т.ч. укр. буквы), цифры, пробел, дефис и подчёркивание
        .replace(/[^a-z0-9а-яіїєґА-ЯІЇЄҐ_\- ]/gu, "")
        .replace(/\s+/g, "_")
        .slice(0, 64);
}
